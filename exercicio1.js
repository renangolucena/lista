
const x = 10;

const y = 10; 

const escolhaOperacao= 1;

switch(escolhaOperacao){

	case 1: 
		console.log(`${x} + ${y}= ${x+y}`)
		break;

	case 2: 
		console.log(`${x} - ${y}= ${x-y}`)
		break;

	case 3: 
		console.log(`${x} * ${y}= ${x*y}`)
		break;

	case 4: 
		console.log(`${x} / ${y}= ${x/y}`)
		break;	

}